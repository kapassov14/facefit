from __future__ import annotations

from typing import Literal

from pydantic import BaseModel, Field

Status = Literal["good", "attention", "priority"]


class SkinAgeCopy(BaseModel):
    value: str = "32"
    unit: str = "лет"
    comment: str = "Кожа выглядит плотной и ухоженной; визуально годы добавляют межбровье, носогубная зона и усталость взгляда."
    score: str = "78/100"


class SkinTypeCopy(BaseModel):
    title: str = "Комби, склонная к обезвоженности"
    bullets: list[str] = Field(
        default_factory=lambda: [
            "Плюс: кожа хорошо держит каркас лица.",
            "Центральная зона просит больше увлажнения.",
            "Кожа может выглядеть ровнее и свежее при мягком уходе.",
        ]
    )


class FaceAgingCopy(BaseModel):
    face_strengths: str = "Мягкая природная форма с хорошей скуловой опорой"
    aging_type: str = "Усталый / смешанный сценарий"
    bullets: list[str] = Field(
        default_factory=lambda: [
            "Скулы уже дают лицу естественный лифтинг.",
            "Глаза — сильная точка притяжения, их важно раскрыть.",
            "Овал сохранён, его лучше поддерживать через шею.",
        ]
    )
    forecast: list[str] = Field(
        default_factory=lambda: [
            "Сейчас: видны первые зоны, которые забирают свежесть.",
            "6–12 месяцев: без поддержки они могут читаться ярче.",
            "1–2 года: системе уже легче работать на профилактику.",
        ]
    )
    strong_base: str = "Форма, скулы и плотность кожи — природный ресурс, который часто ищут в процедурах."


class ZoneCopy(BaseModel):
    number: int
    label: str
    status: Status = "attention"


class GrowthZoneCopy(BaseModel):
    label: str
    status: Status = "attention"


class ProtocolCopy(BaseModel):
    skin_age: SkinAgeCopy = Field(default_factory=SkinAgeCopy)
    skin_type: SkinTypeCopy = Field(default_factory=SkinTypeCopy)
    face_aging: FaceAgingCopy = Field(default_factory=FaceAgingCopy)
    zones: list[ZoneCopy] = Field(default_factory=list)
    causes: list[str] = Field(
        default_factory=lambda: [
            "Шея влияет на отток — взгляд быстрее теряет свежесть.",
            "Межбровье держит напряжение — лицо выглядит строже.",
            "Средняя треть влияет на носогубную зону и мягкость лица.",
        ]
    )
    why_intro: str = "Хорошая база уже есть; то, с чем стоит поработать, связано с шеей, оттоком и мимическим напряжением."
    why_outro: str = "Поэтому система сильнее случайных упражнений: порядок шагов помогает раскрыть природную базу."
    strengths: list[str] = Field(
        default_factory=lambda: [
            "Мягкая природная форма дает лицу выразительность.",
            "Скулы создают естественную лифтинг-опору.",
            "Плотная кожа хорошо держит контур лица.",
        ]
    )
    benefits: list[str] = Field(
        default_factory=lambda: [
            "Раскроет взгляд через расслабление верхней трети.",
            "Сделает скулы визуально выразительнее.",
            "Поможет овалу выглядеть собраннее через шею и лимфу.",
        ]
    )
    benefits_outro: str = "Система Bella Vladi нужна, чтобы поддержать сильные стороны лица в правильной последовательности."
    forecast: list[str] = Field(
        default_factory=lambda: [
            "2 недели — взгляд может стать мягче.",
            "3–4 недели — межбровье спокойнее, лицо открытее.",
            "6–8 недель — по опыту учениц, контур может стать устойчивее.",
        ]
    )
    growth_zones: list[GrowthZoneCopy] = Field(default_factory=list)
    final_summary: str = "В лице уже есть сильная природная база. Главный шаг сейчас — мягко снять лишнее напряжение и поддержать свежесть."
    strict_blocks: dict = Field(default_factory=dict)


